impl From<Datum> for Comment {
    fn from(v: Datum) -> Self {
        Self::Datum(Box::new(v))
    }
}

// ------------------------------------------------------------------------------------------------

impl From<Comment> for Datum {
    fn from(v: Comment) -> Self {
        Self::Comment(v)
    }
}

