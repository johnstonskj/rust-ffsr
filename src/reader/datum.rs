#[derive(Clone, Debug, PartialEq, Eq, Hash)]
pub enum Datum {
    String(String),
    Comment(Comment),
}

#[derive(Clone, Debug, PartialEq, Eq, Hash)]
pub enum Comment {
    Datum(Box<Datum>),
    Block(String),
    Line(String),
}

