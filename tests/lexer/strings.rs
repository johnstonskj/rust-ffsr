// Single-valued success cases
// ------------------------------------------------------------------------------------------------

success_case!(empty, "\"\"" => string);

success_case!(hello, "\"hello\"" => string);

success_case!(hel_mnemonic_escape_lo, "\"hel\\\"lo\"" => string);

success_case!(hel_hex_escape_lo, "\"hel\\x00fd;lo\"" => string);

